"""Regression tests for the DD pipeline extractors and synthesis agent.

Each test loads the corresponding ``.test.json`` golden dataset and runs the
agent harness against it, scoring the output with ``final_response_match_v2``
(LLM judge, threshold 0.8).

Golden datasets are generated by ``capture_golden.py`` and must be committed
to the repository before these tests can pass.  Tests skip automatically when
the golden file does not yet exist, so CI stays green on feature branches
before golden data is captured.

Running
-------
From the backend directory::

    # Run all eval tests (requires GOOGLE_CLOUD_PROJECT and ADC credentials):
    pytest eval/test_extractors.py -v

    # Run a single harness:
    pytest eval/test_extractors.py::test_tabu_extractor -v

Environment variables required
-------------------------------
    GOOGLE_CLOUD_PROJECT   — GCP project for Vertex AI / Gemini API calls
    GOOGLE_GENAI_USE_VERTEXAI=TRUE  (or set GOOGLE_API_KEY for AI Studio)
"""

from __future__ import annotations

import os
from pathlib import Path

import pytest

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

EVAL_DIR = Path(__file__).resolve().parent
CONFIG_FILE = str(EVAL_DIR / "test_config.json")


def _test_file(harness_dir: str, filename: str) -> str:
    return str(EVAL_DIR / harness_dir / filename)


def _skip_if_missing(path: str) -> None:
    """Skip the test if the golden dataset file does not yet exist."""
    if not Path(path).exists():
        pytest.skip(
            f"Golden dataset not found: {path}\n"
            "Run  `python eval/capture_golden.py`  to generate it."
        )


# ---------------------------------------------------------------------------
# Extractor tests
# ---------------------------------------------------------------------------


@pytest.mark.asyncio
async def test_tabu_extractor() -> None:
    """Tabu extractor — nested parcels / sub-parcels / caveats structure."""
    from google.adk.evaluation.agent_evaluator import AgentEvaluator

    test_file = _test_file("tabu_eval_agent", "tabu.test.json")
    _skip_if_missing(test_file)

    await AgentEvaluator.evaluate(
        agent_module="eval.tabu_eval_agent",
        eval_dataset_file_path_or_dir=test_file,
        num_runs=1,
        print_detailed_results=True,
    )


